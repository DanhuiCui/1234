    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String age = txtAge.getText();
        String gender = txtGender.getText();
        String birthday = txtBirthday.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        
        if(name.isEmpty()||age.isEmpty()||gender.isEmpty()||birthday.isEmpty()||phone.isEmpty()||address.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter complete information!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!checkAge(age)) {
            JOptionPane.showMessageDialog(null, "Age should be an integer between 0 and 100!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }

        int a = Integer.parseInt(age);
        if (a<=0 || a>100) {    
            JOptionPane.showMessageDialog(null, "Age should be an integer between 0 and 100!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(!checkDate(birthday)){
            JOptionPane.showMessageDialog(null, "Birthday format error!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!checkAge(phone)) {
            JOptionPane.showMessageDialog(null, "Phone number format error!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!gender.equals("male")&&!gender.equals("female")) {
            JOptionPane.showMessageDialog(null, "Please enter female or male!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
            
  

        userAccount.getEmployee().setName(name);
        userAccount.getEmployee().setAddress(address);
        userAccount.getEmployee().setAge(Integer.parseInt(age));
        userAccount.getEmployee().setBirthday(birthday);
        userAccount.getEmployee().setGender(gender);
        userAccount.getEmployee().setPhone(phone);
        
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        
        txtAddress.setEnabled(false);
        txtAge.setEnabled(false);
        txtBirthday.setEnabled(false);
        txtGender.setEnabled(false);
        txtName.setEnabled(false);
        txtPhone.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

