    public void populateOrder(){
        DefaultTableModel dtm = (DefaultTableModel) tabOrder.getModel();
        dtm.setRowCount(0);
        for(Order order: userAccount.getWorkQueue().getOrderList()){
            Object[] row = new Object[5];
            row[0] = order.getMessage();         
            row[1] = order.getReceiver();
            row[2] = order.getStatus(); 
            row[3] = order.getRequestTime(); 
            row[4] = order.getResolveTime(); 
            dtm.addRow(row);
        }
        TableColumn column1 = null;
        column1 = tabOrder.getColumnModel().getColumn(0);
        column1.setPreferredWidth(250);
        
        TableColumn column2 = null;
        column2 = tabOrder.getColumnModel().getColumn(3);
        column2.setPreferredWidth(150);
        
        TableColumn column3 = null;
        column3 = tabOrder.getColumnModel().getColumn(4);
        column3.setPreferredWidth(150);
    }
    
    static boolean checkAge(String age) {
        Pattern p = Pattern.compile("^[1-9]\\d*|0$");
        return p.matcher(age).matches();
    }
    
    static boolean checkPhone(String phone) {
        Pattern p = Pattern.compile("^[0-9]*$");
        return p.matcher(phone).matches();
    }
    
    static boolean checkDate(String date) {
        Pattern p = Pattern.compile("^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|[13579][26])00)-02-29)$");
        return p.matcher(date).matches();
    }

