    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Network network = (Network) networkJComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        
        String name = nameJTextField.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(null, "Please input the name of enterprise!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        for (Network n : system.getNetworkList()) {
            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                if(e.getEnterpriseType().Population == enterpriseTypeJComboBox.getSelectedItem()){
                    List<Enterprise> list1 = new ArrayList<Enterprise>();
                    list1.add(e);
                    for (Enterprise e1 : list1){
                        if(e1.getName().equals(name)){
                            JOptionPane.showMessageDialog(null, "This name has existed in this type!","Warning",JOptionPane.WARNING_MESSAGE);
                            return;
                        }
                    }
                }
                if(e.getEnterpriseType().Gym == enterpriseTypeJComboBox.getSelectedItem()){
                    List<Enterprise> list1 = new ArrayList<Enterprise>();
                    list1.add(e);
                    for (Enterprise e1 : list1){
                        if(e1.getName().equals(name)){
                            JOptionPane.showMessageDialog(null, "This name has existed in this type!","Warning",JOptionPane.WARNING_MESSAGE);
                            return;
                        }
                    }
                }
                if(e.getEnterpriseType().Restaurant == enterpriseTypeJComboBox.getSelectedItem()){
                    List<Enterprise> list1 = new ArrayList<Enterprise>();
                    list1.add(e);
                    for (Enterprise e1 : list1){
                        if(e1.getName().equals(name)){
                            JOptionPane.showMessageDialog(null, "This name has existed in this type!","Warning",JOptionPane.WARNING_MESSAGE);
                            return;
                        }
                    }
                }
                if(e.getEnterpriseType().PhysicalCenter == enterpriseTypeJComboBox.getSelectedItem()){
                    List<Enterprise> list1 = new ArrayList<Enterprise>();
                    list1.add(e);
                    for (Enterprise e1 : list1){
                        if(e1.getName().equals(name)){
                            JOptionPane.showMessageDialog(null, "This name has existed in this type!","Warning",JOptionPane.WARNING_MESSAGE);
                            return;
                        }
                    }
                }
            }
        }

        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);

        populateTable();
        nameJTextField.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed

