    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Type type = (Type) organizationJComboBox.getSelectedItem();
        String name = txtName.getText();
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter the name!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization.getName().equals(name)){
                JOptionPane.showMessageDialog(null, "This name has existed!","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            
        }
        
        Organization organization = enterprise.getOrganizationDirectory().createOrganization(type);
        organization.setName(name);
        populateTable();
        
        //this.organizationJComboBox.setSelectedItem(enterprise.getOrganizationDirectory().getOrganizationList().get(0));
        this.txtName.setText("");
        
            
            
            
        
    }//GEN-LAST:event_addJButtonActionPerformed

